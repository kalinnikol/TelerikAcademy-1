namespace _2.ComparePerfOfNumberTypes
{
    public class IncrementionMethods
    {
        public static void IncrementInt(int startValue, int numberOfRepetitions)
        {
            for (int i = 0; i < numberOfRepetitions; i++)
            {
                startValue++;
            }
        }

        public static void IncrementLong(long startValue, int numberOfRepetitions)
        {
            for (int i = 0; i < numberOfRepetitions; i++)
            {
                startValue++;
            }
        }

        public static void IncrementFloat(float startValue, int numberOfRepetitions)
        {
            for (int i = 0; i < numberOfRepetitions; i++)
            {
                startValue++;
            }
        }

        public static void IncrementDouble(double startValue, int numberOfRepetitions)
        {
            for (int i = 0; i < numberOfRepetitions; i++)
            {
                startValue++;
            }
        }

        public static void IncrementDecimal(decimal startValue, int numberOfRepetitions)
        {
            for (int i = 0; i < numberOfRepetitions; i++)
            {
                startValue++;
            }
        }
    }
}
